void main() {
  String texto_1 = " Eu amo minha mãe";
  String texto_2 = 'e Eu amo meu pai';
  String concatenar = texto_1 + texto_2;
  print(concatenar); //usado para chamar uma variável por função print(), caso você queira chamar mais, use o operador matemático +
  print("$concatenar"); //pode ser usado para chamar uma ou mais variáveis
}
//O tipo string armazena textos, os textos devem vir obrigatoriamente entre aspas
//simples ou duplas, dependendo da sua necessidade
//a palavra String deve vir com letra maiuscula, já que é uma classe reservada 
//a String concatenar foi utilizada para somar informações, o sinal de mais serve como um "e"
